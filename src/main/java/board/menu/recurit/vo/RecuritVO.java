package board.menu.recurit.vo;



public class RecuritVO {
	String username;
	Integer age;
	String birth;
	String college_name;
	String college_magor;
	String support_job_name;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getCollege_name() {
		return college_name;
	}
	public void setCollege_name(String college_name) {
		this.college_name = college_name;
	}
	public String getCollege_magor() {
		return college_magor;
	}
	public void setCollege_magor(String college_magor) {
		this.college_magor = college_magor;
	}
	public String getSupport_job_name() {
		return support_job_name;
	}
	public void setSupport_job_name(String support_job_name) {
		this.support_job_name = support_job_name;
	}
	
}
